/* ১. Math দিয়ে min, max, ceil, floor, abs, round, random ইত্যাদি দেখে রাখতে হবে। যদি লাগে যেন বের করে ফেলতে পারো  */
console.log(Math.min(5, 7, 8, 4));
console.log(Math.max(5, 7, 8, 4));
console.log(Math.ceil(12.01));
console.log(Math.floor(12.99));
console.log(Math.round(12.49));
console.log(Math.round(12.5));
console.log(Math.abs(-15));
console.log(Math.round(Math.random() * 100));